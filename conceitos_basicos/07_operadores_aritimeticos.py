print(1 + 1) #soma
#>>> 2
print(10 -2) #subtração
#>>> 8
print(4 * 3) #multiplicação
#>>> 12
print(12 / 3) #divisão
#>>> 4.0
print(12 // 2) #divisão inteira
#>>> 6
print(10 % 3) #módulo
#>>> 1
print(2 ** 3) #Exponenciação
#>>> 8

"""Precedência

1) Parênteses
2) Expoêntes
3) Multiplicações e divisões (da esquerda para a direita)
4) Somas e subtração (da esquerda para a direita)
"""
print(10 - 5 * 2)
#>>> 0
print((10 -5) * 2)
#>>> 10
print(10 ** 2 * 2)
#>>> 200
print(10 ** (2 * 2))
#>>> 10000
print(10 / 2 * 4)
#>>> 20.0